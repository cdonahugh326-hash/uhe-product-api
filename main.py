from fastapi import FastAPI, Query
from fastapi.middleware.cors import CORSMiddleware
import pandas as pd

app = FastAPI()

app.add_middleware(
    CORSMiddleware,
    allow_origins=["*"],
    allow_credentials=True,
    allow_methods=["*"],
    allow_headers=["*"],
)

# Load Excel file into memory
df = pd.read_excel('UHE_Visual_Product_Catalog_AutoGenerated.xlsx')

@app.get("/")
def read_root():
    return {"message": "UHE Product API is Live!"}

@app.get("/getProduct")
def get_product(productName: str = Query(..., description="Product name to look up")):
    result = df[df['Product Name'].str.lower() == productName.lower()]
    if result.empty:
        return {"error": "Product not found"}
    return result.to_dict(orient='records')[0]
